package alarm;

public class AlarmDataBean {

	private Integer aNum;
	private Integer aCauseNum;
	private String receiver;
	private String aMsg;
	
	public Integer getaNum() {
		return aNum;
	}
	public Integer getaCauseNum() {
		return aCauseNum;
	}
	public String getReceiver() {
		return receiver;
	}
	public String getaMsg() {
		return aMsg;
	}
	public void setaNum(Integer aNum) {
		this.aNum = aNum;
	}
	public void setaCauseNum(Integer aCauseNum) {
		this.aCauseNum = aCauseNum;
	}
	public void setReceiver(String receiver) {
		this.receiver = receiver;
	}
	public void setaMsg(String aMsg) {
		this.aMsg = aMsg;
	}
	
	
}
